    WITH ventas_promedio AS (
        SELECT
            vendedor_id,
            DATE_TRUNC('month', fecha) as mes,
            AVG(importe) as promedio_importe
        FROM transacciones
        GROUP BY vendedor_id, mes
    ),
    ventas_con_lag AS (
        SELECT vendedor_id,
               mes, round(promedio_importe, 2) as promedio,
               round(lag(promedio_importe, 1) over (PARTITION BY vendedor_id ORDER BY mes), 2) as lag_mes1,
               round(lag(promedio_importe, 2) over (PARTITION BY vendedor_id ORDER BY mes), 2) as lag_mes2
        FROM ventas_promedio
    )

    SELECT
        vendedor_id,
        mes,
        promedio,
        lag_mes1,
        lag_mes2
    FROM ventas_con_lag
    WHERE promedio > lag_mes1 AND lag_mes1 > lag_mes2 AND lag_mes1 IS NOT NULL AND lag_mes2 IS NOT NULL
    ORDER BY vendedor_id, mes;

